# list = cualquier tipo(str, int, float, boolean). Tambien puedes combinarlos
# Ej: ["hola", 4, 5.6, True]

nombres = ["Joel", "Cristian", "Lucia", "Sebastian", "Johana", "Jackeline"]
print(nombres) # Imprime la lista completa
print(nombres[0]) # Imprime el primer elemento de la lista
print(nombres[3]) # Imprime el cuarto elemento de la lista
print(nombres[-1]) # Imprime el ultimo elemento de la lista. Los numeros negativos comienzan desde el final